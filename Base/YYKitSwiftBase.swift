import Foundation
import UIKit
import QuartzCore

// MARK: - YYKitSwift 基础扩展模块

/// YYKitSwift 基础扩展模块
///
/// 此模块提供 Foundation、UIKit 和 Quartz 的扩展，包括：
/// - 常用类型的 `.ls` 命名空间扩展
/// - UI 组件的便捷方法
/// - 图形和动画扩展
///
/// ## 主要分类
///
/// - **Foundation**: String、Array、Dictionary、Date、Data 等
/// - **UIKit**: UIView、UIImage、UIColor、UILabel、UIFont 等
/// - **Quartz**: CALayer、CGUtilities 等
///
/// ## 使用示例
///
/// ```swift
/// // 使用命名空间扩展
/// view.ls.width = 100
/// view.ls.height = 200
///
/// // 颜色扩展
/// let color = UIColor.ls.hex(0xFF0000)
/// ```
///
/// ## 依赖关系
///
/// - ✅ **完全独立**: 不依赖任何其他 YYKitSwift 模块
/// - ⚠️ **依赖 Core**: 需要引入 YYKitSwiftCore
///
// MARK: - 模块说明

/// 此模块包含大量扩展文件，由于数量众多，此处不逐一导出。
/// 扩展会自动加载，无需手动导入。
///
// MARK: - Foundation 扩展
/// Foundation 扩展包括：
